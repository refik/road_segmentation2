import os
from typing import List

import matplotlib.image as mpimg
import numpy as np
from PIL import Image


def img_float_to_uint8(img):
    rimg = img - np.min(img)
    rimg = (rimg / np.max(rimg) * 255).round().astype(np.uint8)
    return rimg


def make_img_overlay_gt_pred(img, gt_img, pred_img):
    w = img.shape[0]
    h = img.shape[1]
    correct_pred = gt_img * pred_img + ((1 - gt_img) * (1 - pred_img))
    wrong_pred = 1 - correct_pred
    color_mask = np.zeros((w, h, 3), dtype=np.uint8)
    # color_mask[:,:,0] = gt_img*255
    color_mask[:, :, 0] = wrong_pred * 255
    color_mask[:, :, 1] = correct_pred * 255

    img8 = img_float_to_uint8(img)
    background = Image.fromarray(img8, 'RGB').convert("RGBA")
    overlay = Image.fromarray(color_mask, 'RGB').convert("RGBA")
    new_img = Image.blend(background, overlay, 0.2)
    return new_img


